using System.Collections.Generic;
using System.Linq;
using ApplicationTracker.Models;
using ApplicationTracker.Data;
using Microsoft.EntityFrameworkCore;

namespace ApplicationTracker.Services
{
    /// <summary>
    /// Database-backed implementation of application CRUD operations
    /// using Entity Framework Core and SQLite.
    /// All operations are asynchronous and use EF Core async APIs.
    /// </summary>
    public class ApplicationService : IApplicationService
    {
        private readonly ApplicationDbContext _db;
        public ApplicationService(ApplicationDbContext db)
        {
            _db = db;
        }

        /// <summary>
        /// Creates a new application and persists it to the database.
        /// The Id is generated by the database.
        /// </summary>
        public async Task<ApplicationModel> Create(ApplicationModel application)
        {
            _db.Applications.Add(application);
            await _db.SaveChangesAsync();
            return application;
        }

        /// <summary>
        /// Returns all stored applications.
        /// </summary>
        public async Task<List<ApplicationModel>> GetAll()
        {
            return await _db.Applications.ToListAsync();
        }

        /// <summary>
        /// Retrieves a single application by Id.
        /// Returns null if not found.
        /// </summary>
        public async Task<ApplicationModel?> GetById(int id)
        {
            return await _db.Applications.FirstOrDefaultAsync(a => a.Id == id);
        }

        /// <summary>
        /// Updates an existing application.
        /// Returns true if the application was found and updated,
        /// false if no matching Id exists.
        /// </summary>
        public async Task<bool> Update(int id, ApplicationModel updatedApplication)
        {
            var existing = await _db.Applications.FirstOrDefaultAsync(a => a.Id == id);
            if (existing == null)
                return false;
            
            existing.CompanyName = updatedApplication.CompanyName;
            existing.DateApplied = updatedApplication.DateApplied;
            existing.Status = updatedApplication.Status;
            existing.Notes = updatedApplication.Notes;

            await _db.SaveChangesAsync();
            return true;
        }

        /// <summary>
        /// Deletes an application by Id.
        /// Returns true if deletion occurred,
        /// false if the Id was not found.
        /// </summary>
        public async Task<bool> Delete(int id)
        {
            var existing =  await _db.Applications.FirstOrDefaultAsync(a => a.Id == id);
            if (existing == null)
                return false;

            _db.Applications.Remove(existing);
            await _db.SaveChangesAsync();
            return true;
        }
    }
}
